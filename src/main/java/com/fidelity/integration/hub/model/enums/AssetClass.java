package com.fidelity.integration.hub.model.enums;

/**
 * Asset class enumeration.
 * Represents the classification of financial instruments.
 */
public enum AssetClass {
    /** Equities (stocks) */
    EQUITY,
    
    /** Fixed income (bonds) */
    FIXED_INCOME,
    
    /** Cash and cash equivalents */
    CASH,
    
    /** Commodities */
    COMMODITY,
    
    /** Real estate investment trusts */
    REIT,
    
    /** Exchange-traded funds */
    ETF,
    
    /** Mutual funds */
    MUTUAL_FUND,
    
    /** Options */
    OPTION,
    
    /** Futures */
    FUTURE,
    
    /** Cryptocurrency */
    CRYPTO
}
